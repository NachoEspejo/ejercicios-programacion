/**
 *Ejercicio 1
 *Muestra los números múltiplos de 5 de 0 a 100 utilizando un bucle for .
 */
 
public class Ejercicio01 {
  
  public static void main(String[] args) {
    
    System.out.println("Procesando...");
    
    for(int i = 0; i <= 100; i += 5) {
      System.out.println(i);
    }
  }
}
